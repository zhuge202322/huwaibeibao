export function getLanguageMeta(code: LanguageCode) {
  return SUPPORTED_LANGUAGES.find((language) => language.code === code) || SUPPORTED_LANGUAGES[0];
}

export function translateText(text: string, language: LanguageCode): string {
  if (language === "EN") return text;

  const normalized = text.trim();
  if (!normalized) return text;

  const dictionary = {
    ...exactTranslations[language],
    ...additionalExactTranslations[language],
  };
  if (dictionary[normalized]) return dictionary[normalized];

  const itemMatch = normalized.match(/^(\d+)\s+Items$/);
  if (itemMatch) return language === "DE" ? `${itemMatch[1]} Artikel` : `${itemMatch[1]} artículos`;

  const postMatch = normalized.match(/^(\d+)\s+Posts$/);
  if (postMatch) return language === "DE" ? `${postMatch[1]} Beiträge` : `${postMatch[1]} publicaciones`;

  const foundMatch = normalized.match(/^Found\s+(\d+)\s+products$/);
  if (foundMatch) return language === "DE" ? `${foundMatch[1]} Produkte gefunden` : `${foundMatch[1]} productos encontrados`;

  const pageMatch = normalized.match(/^Page\s+(\d+)\s+of\s+(\d+)\s+\(Total\s+(\d+)\s+items\)$/);
  if (pageMatch) {
    return language === "DE"
      ? `Seite ${pageMatch[1]} von ${pageMatch[2]} (insgesamt ${pageMatch[3]} Artikel)`
      : `Página ${pageMatch[1]} de ${pageMatch[2]} (total ${pageMatch[3]} artículos)`;
  }

  const moqMatch = normalized.match(/^MOQ:\s+(\d+)\s+(pcs|Pcs)$/);
  if (moqMatch) return language === "DE" ? `MOQ: ${moqMatch[1]} Stk.` : `MOQ: ${moqMatch[1]} uds.`;

  const moqPiecesMatch = normalized.match(/^MOQ:\s+(\d+)\s+Pieces$/);
  if (moqPiecesMatch) return language === "DE" ? `MOQ: ${moqPiecesMatch[1]} Stück` : `MOQ: ${moqPiecesMatch[1]} piezas`;

  const leadMatch = normalized.match(/^Lead:\s+(\d+)\s+Days$/);
  if (leadMatch) return language === "DE" ? `Lieferzeit: ${leadMatch[1]} Tage` : `Plazo: ${leadMatch[1]} días`;

  const daysMatch = normalized.match(/^(\d+)\s+Days$/);
  if (daysMatch) return language === "DE" ? `${daysMatch[1]} Tage` : `${daysMatch[1]} días`;

  return phraseTranslations[language].reduce((current, [pattern, replacement]) => current.replace(pattern, replacement), normalized);
}
